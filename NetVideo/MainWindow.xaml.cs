    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void Button_Click(object sender, RoutedEventArgs e)
        {
            int index = int.Parse(((Button)e.Source).Uid);

            GridCursor.Margin = new Thickness(110 + (100 * index), 50, 0, 0);
            switch (index)
            {
                case 4:
                    {
                        this.Close();
                        break;
                    }
            }
        }
    }
